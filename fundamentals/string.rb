# Dealling with Strings
phrase = "Getting Started with Ruby"

# Prints a variable
puts phrase

# Prints on upercase letters
puts phrase.upcase()

# Prints on downcase letters
puts phrase.downcase()

# Prints without unnecessary spaces
puts phrase.strip()

# Prints true of false for include condition
puts phrase.include? "Ruby"

# Prints get phrase length
puts phrase.length()

# Get a specific char in phrase
puts phrase[0]

# Get char index
puts phrase.index("R")
