# Handling with math

# Prints an integer
puts 5

# Prints a float
puts 5.2342

# Prints a sum
puts 5 + 7

# Prints a multiplication
puts 5 * 7

# Prints a division
puts 35 / 5

# Prints a Square
puts 2**

# Prints positive number
num = -20
puts num.abs()

# Prints rounded value
num = 20.687
puts num.round()

# Prints next lower number
num = 20.9
puts num.floor()

# Square root
puts Math.sqrt(36)
